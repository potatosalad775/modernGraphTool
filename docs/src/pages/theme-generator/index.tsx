// ── Component ────────────────────────────────────────────────────────────────

type SemanticColorKey =
	| 'primary'
	| 'secondary'
	| 'accent'
	| 'info'
	| 'success'
	| 'warning'
	| 'error';

export default function ThemeGenerator(): ReactNode {
	const [inputs, setInputs] = useState<PaletteInputs>(DEFAULT_INPUTS);
	const [lightTheme, setLightTheme] = useState<ThemePalette>(
		() => generateFullPalette(DEFAULT_INPUTS).light
	);
	const [darkTheme, setDarkTheme] = useState<ThemePalette>(
		() => generateFullPalette(DEFAULT_INPUTS).dark
	);
	const [activePicker, setActivePicker] = useState<string | null>(null);

	useEffect(() => {
		const timeoutId = setTimeout(() => {
			const { light, dark } = generateFullPalette(inputs);
			setLightTheme(light);
			setDarkTheme(dark);
		}, 50);
		return () => clearTimeout(timeoutId);
	}, [inputs]);

	const updateColor = (key: SemanticColorKey) => (color: ColorResult) => {
		setInputs((prev) => ({ ...prev, [key]: color.hex }));
	};

	const handleBaseHueChange = (value: number) => {
		setInputs((prev) => ({ ...prev, baseHue: Math.max(0, Math.min(360, value)) }));
	};

	const handleRandomAll = () => {
		setInputs({
			baseHue: Math.floor(Math.random() * 360),
			primary: randomHex(),
			secondary: randomHex(),
			accent: randomSemanticHex(280, 60, [0.4, 0.55], [0.01, 0.06]),
			info: randomSemanticHex(220, 40, [0.6, 0.75], [0.12, 0.2]),
			success: randomSemanticHex(130, 40, [0.65, 0.8], [0.15, 0.25]),
			warning: randomSemanticHex(75, 30, [0.7, 0.85], [0.14, 0.22]),
			error: randomSemanticHex(15, 20, [0.55, 0.7], [0.18, 0.26])
		});
	};

	const handleExport = () => {
		const css = generateThemeCssContent(lightTheme, darkTheme);
		const blob = new Blob([css], { type: 'text/css' });
		const url = URL.createObjectURL(blob);
		const a = document.createElement('a');
		a.href = url;
		a.download = 'theme.css';
		document.body.appendChild(a);
		a.click();
		document.body.removeChild(a);
		URL.revokeObjectURL(url);
	};

	const togglePicker = (id: string) => {
		setActivePicker((prev) => (prev === id ? null : id));
	};

	// ── Render helpers ───────────────────────────────────────────────────────

	const renderColorPicker = (
		id: string,
		label: string,
		colorHex: string,
		onChange: (color: ColorResult) => void
	) => (
		<div className={styles.tgColorPickerWrapper} key={id}>
			<label>{label}</label>
			<button
				type="button"
				className={styles.tgColorButton}
				style={{ backgroundColor: colorHex }}
				onClick={() => togglePicker(id)}
			>
				{colorHex}
			</button>
			{activePicker === id && (
				<div className={styles.tgPickerPopover}>
					<div className={styles.tgPickerCover} onClick={() => setActivePicker(null)} />
					<SketchPicker
						className={styles.tgPickerElement}
						color={colorHex}
						onChange={onChange}
						disableAlpha={true}
						presetColors={PICKER_PRESETS}
					/>
				</div>
			)}
		</div>
	);

	return (
		<Layout title="Theme Generator" description="Theme Generator for modernGraphTool">
			<div className={styles.tgContainer}>
				<div className={styles.tgTools}>
					<h4>
						<Translate>Base Tone</Translate>
					</h4>
					<p className={styles.tgDescription}>
						<Translate>
							Controls the neutral base palette (backgrounds, text, borders). Adjust the hue to tint
							your UI surface.
						</Translate>
					</p>
					<section className={styles.tgBaseHueSection}>
						<label htmlFor="baseHueSlider">
							<Translate>Base Hue</Translate>
						</label>
						<div className={styles.tgBaseHueInputRow}>
							<input
								id="baseHueSlider"
								type="range"
								min="0"
								max="360"
								step="1"
								value={inputs.baseHue}
								className={styles.tgHueSlider}
								onChange={(e) => handleBaseHueChange(parseInt(e.target.value, 10))}
							/>
							<input
								type="number"
								min="0"
								max="360"
								step="1"
								value={inputs.baseHue}
								className={styles.tgNumberInput}
								onChange={(e) => handleBaseHueChange(parseInt(e.target.value, 10) || 0)}
							/>
						</div>
					</section>

					<h4>
						<Translate>Semantic Colors</Translate>
					</h4>
					<p className={styles.tgDescription}>
						<Translate>
							Pick colors for each role. Content (text-on-color) and dark mode variants are
							auto-generated.
						</Translate>
					</p>

					<section className={styles.tgSourceColorSection}>
						{renderColorPicker('primary', 'Primary', inputs.primary, updateColor('primary'))}
						<button
							type="button"
							className={styles.tgSourceColorRandomizerButton}
							onClick={handleRandomAll}
						>
							<Translate>Random All</Translate>
						</button>
					</section>

					{renderColorPicker('secondary', 'Secondary', inputs.secondary, updateColor('secondary'))}
					{renderColorPicker('accent', 'Accent', inputs.accent, updateColor('accent'))}

					<h4>
						<Translate>Status Colors</Translate>
					</h4>
					{renderColorPicker('info', 'Info', inputs.info, updateColor('info'))}
					{renderColorPicker('success', 'Success', inputs.success, updateColor('success'))}
					{renderColorPicker('warning', 'Warning', inputs.warning, updateColor('warning'))}
					{renderColorPicker('error', 'Error', inputs.error, updateColor('error'))}

					<button type="button" onClick={handleExport} className={styles.tgExportButton}>
						<Translate>Export theme.css</Translate>
					</button>
				</div>
				<div className={styles.tgPreview}>
					<DummyGraphPage colorMode="light" theme={lightTheme} />
					<DummyGraphPage colorMode="dark" theme={darkTheme} />
				</div>
			</div>
		</Layout>
	);
}
