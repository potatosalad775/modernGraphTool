// ── Random color generation helpers ──────────────────────────────────────────

function randomHex(): string {
	return (
		'#' +
		Math.floor(Math.random() * 16777215)
			.toString(16)
			.padStart(6, '0')
	);
}

function randomSemanticHex(
	hueCenter: number,
	hueSpread: number,
	lRange: [number, number],
	cRange: [number, number]
): string {
	const h = hueCenter + (Math.random() - 0.5) * hueSpread;
	const l = lRange[0] + Math.random() * (lRange[1] - lRange[0]);
	const c = cRange[0] + Math.random() * (cRange[1] - cRange[0]);
	return oklchToHex({ l, c, h: ((h % 360) + 360) % 360 });
}

